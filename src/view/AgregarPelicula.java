    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Registro registro=new Registro();
            Pelicula pelicula=new Pelicula();
            
            
            try{
                Categoria categoria=registro.VerificarC(Integer.parseInt(jTextField1.getText().trim()));
                
                int cod=Integer.parseInt(jTextField2.getText().trim());
                if(cod>=10000 && cod<=99999){pelicula.setCodigo(Integer.parseInt(jTextField2.getText().trim()));}
                else{javax.swing.JOptionPane.showMessageDialog(this, "Código debe estar entre 10000 y 99999");throw new IllegalArgumentException();}
                
                int pre=Integer.parseInt(jTextField3.getText().trim());
                if(pre>1000){pelicula.setPrecio(Integer.parseInt(jTextField3.getText().trim()));}
                else{javax.swing.JOptionPane.showMessageDialog(this, "Precio debe ser mayor a 1000");throw new IllegalArgumentException();}
                
                pelicula.setId(categoria.getId());
                
                if(jTextField4.getText().trim().length()>=3){pelicula.setNombre(jTextField4.getText().trim());}
                else{javax.swing.JOptionPane.showMessageDialog(this, "Nombre debe tener al menos 3 carácteres"); throw new IllegalArgumentException();}
                
                if(jRadioButton1.isSelected()){pelicula.setFormato4k("S");}
                if(jRadioButton2.isSelected()){pelicula.setFormato4k("N");}
                if(jRadioButton1.isSelected()==false && jRadioButton2.isSelected()==false){throw new IllegalArgumentException();}
                
                pelicula.setDescripcion(categoria.getDescripcion());
                
                if(registro.GrabarP(pelicula)){javax.swing.JOptionPane.showMessageDialog(this, "Pelicula Guardada");}
                else{javax.swing.JOptionPane.showMessageDialog(this, "Película No Guardada,posible código repetido");}
            }
            catch(IllegalArgumentException g){javax.swing.JOptionPane.showMessageDialog(this, "Valores Inválidos o no Ingresados");}
    }//GEN-LAST:event_jButton5ActionPerformed

