    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Registro registro=new Registro();
        try{
            if(registro.Check(Integer.parseInt(jTextField1.getText().trim()))){
                Categoria categoria=registro.VerificarC(Integer.parseInt(jTextField1.getText().trim()));
                javax.swing.JOptionPane.showMessageDialog(this, "Categoría Encontrada "+categoria.getDescripcion());
                jTextField1.setEnabled(false);
                jButton1.setEnabled(false);
                jTextField2.setEnabled(true);
                jTextField3.setEnabled(true);
                jTextField4.setEnabled(true);
                jRadioButton1.setEnabled(true);
                jRadioButton2.setEnabled(true);
                jButton5.setEnabled(true);
            }
            else{javax.swing.JOptionPane.showMessageDialog(this, "Categoría No Encontrada");}
        }
        catch(IllegalArgumentException g){javax.swing.JOptionPane.showMessageDialog(this, "Ingrese Código Numérico");}
    }//GEN-LAST:event_jButton1ActionPerformed

