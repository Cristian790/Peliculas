    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Pelicula pelicula;
       Registro registro=new Registro();
       try{
           if(registro.CheckP(Integer.parseInt(jTextField1.getText().trim()))){
               
               pelicula=registro.VerificarP(Integer.parseInt(jTextField1.getText().trim()));
               System.out.println(pelicula.toString());
                jTextField2.setText(String.valueOf(pelicula.getId()));
                jTextField3.setText(String.valueOf(pelicula.getPrecio()));
                if(pelicula.getFormato4k().equals("S")){
                    jRadioButton1.setSelected(true);
                }
                else{jRadioButton2.setSelected(true);}
                jTextField4.setText(pelicula.getNombre());
           }
           else{javax.swing.JOptionPane.showMessageDialog(this, "Pel√≠cula No encontrada");}
       }
       catch(IllegalArgumentException g){javax.swing.JOptionPane.showMessageDialog(this,"Ingrese C√≥digo Num√©rico");}
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Registro registro=new Registro();
        Pelicula pelicula=new Pelicula();
        try{
            Categoria categoria;
            categoria=registro.VerificarC(Integer.parseInt(jTextField2.getText().trim()));
            
            pelicula.setId(Integer.parseInt(jTextField2.getText().trim()));
            
            int pre=Integer.parseInt(jTextField3.getText().trim());
            if(pre>1000){pelicula.setPrecio(Integer.parseInt(jTextField3.getText().trim()));}
            else{javax.swing.JOptionPane.showMessageDialog(this, "Precio debe ser mayor a 1000");throw new IllegalArgumentException();}
            
            
            if(jTextField4.getText().trim().length()>=3){pelicula.setNombre(jTextField4.getText().trim());}
            else{javax.swing.JOptionPane.showMessageDialog(this, "Nombre debe tener al menos 3 car√°cteres"); throw new IllegalArgumentException();}
                
            if(jRadioButton1.isSelected()){pelicula.setFormato4k("S");}
            if(jRadioButton2.isSelected()){pelicula.setFormato4k("N");}
            if(jRadioButton1.isSelected()==false && jRadioButton2.isSelected()==false){throw new IllegalArgumentException();}
            
            
            
            pelicula.setDescripcion(categoria.getDescripcion());
            pelicula.setCodigo(Integer.parseInt(jTextField1.getText().trim()));
           
            
            if(registro.Modificar(pelicula)){javax.swing.JOptionPane.showMessageDialog(this, "Pelicula Modificada");}
            else{javax.swing.JOptionPane.showMessageDialog(this, "Pel√≠cula No Modificada");}
        }
        catch(IllegalArgumentException g){javax.swing.JOptionPane.showMessageDialog(this,"Valores Inv√°lidos o no Ingresados");}
    }//GEN-LAST:event_jButton4ActionPerformed

